import pytest
from sudoku import solve  # Import the solve function


# Function to count failed tests (bugs)
def count_failed_tests():
    # Run pytest in a subprocess and capture the output
    result = pytest.main(["--maxfail=5", "--disable-warnings", "--tb=short", "test_sudoku.py"])

    # Count the number of failures in the result output
    failed_tests = result.value.count('FAILED')
    print(f"Number of bugs (failed tests): {failed_tests}")


def test_solve_valid():
    board = [
        [5, 3, 0, 0, 7, 0, 0, 0, 0],
        [6, 0, 0, 1, 9, 5, 0, 0, 0],
        [0, 9, 8, 0, 0, 0, 0, 6, 0],
        [8, 0, 0, 0, 6, 0, 0, 0, 3],
        [4, 0, 0, 8, 0, 3, 0, 0, 1],
        [7, 0, 0, 0, 2, 0, 0, 0, 6],
        [0, 6, 0, 0, 0, 0, 2, 8, 0],
        [0, 0, 0, 4, 1, 9, 0, 0, 5],
        [0, 0, 0, 0, 8, 0, 0, 7, 9]
    ]
    assert solve(board) == True  # Puzzle is solvable


def test_solve_invalid():
    board = [
        [5, 3, 0, 0, 7, 0, 0, 0, 0],
        [6, 0, 0, 1, 9, 5, 0, 0, 0],
        [0, 9, 8, 0, 0, 0, 0, 6, 0],
        [8, 0, 0, 0, 6, 0, 0, 0, 3],
        [4, 0, 0, 8, 0, 3, 0, 0, 1],
        [7, 0, 0, 0, 2, 0, 0, 0, 6],
        [0, 6, 0, 0, 0, 0, 2, 8, 0],
        [0, 0, 0, 4, 1, 9, 0, 0, 5],
        [0, 0, 0, 0, 8, 0, 0, 7, 9]
    ]
    board[0][0] = 9  # Making the board unsolvable
    assert solve(board) == False  # Puzzle is unsolvable